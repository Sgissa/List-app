 function itemChecked(element, listIndex, itemIndex){
    
    $(element).parent().toggleClass("strike");
    let checkedValue = !LISTS[listIndex].items[itemIndex].checked;
    LISTS[listIndex].items[itemIndex].checked = checkedValue;
    // console.log("checked ", LISTS);
 }

function addItem(listIndex){
    let newItemName = $("#addItem").val();
    let newItemObj = {
        name: newItemName,
        checked: false,
    };
    LISTS[listIndex].items.push(newItemObj);
    loadListsItems(listIndex)
}

function deleteItem(listIndex, idx){
   LISTS[listIndex].items.splice(idx,1);
   loadListsItems(listIndex);
}

function loadListsItems(listIndex){
    let listString = `<button class="backBtn" onclick="loadLists()">Back</button><ul>`
    $.each(LISTS[listIndex].items,function(idx, item){
        listString += `<li id="${idx}" class="${item.checked ? "strike" : ""}"><input ${item.checked ? (checked = "checked") : ""} type="checkbox" id="${idx}" name="${item.name}" onclick="itemChecked(this, ${listIndex}, ${idx})"><span>${item.name}</span>
        <span class="delete" onclick="deleteItem(${listIndex}, ${idx})">delete</span></li>`;
    });

    listString += `</ul>
    <div class="addItemInput">
    <input id="addItem" type="text">
    <button class="addBtn" onclick="addItem(${listIndex})">Add Item</button>
    </div>`;
    $("#app").html(listString);
}


function loadLists(){
    let listString = "<ul>"
    $.each(LISTS,function(idx, list){
        listString += `<li id="${idx}" onclick="loadListsItems(${idx})">${list.name} <span class="right">Items: ${list.items.length}</span></li>`;
    });

    listString += "</ul>";
    $("#app").html(listString);
}

function initListeners() {}

$(document).ready(function(){
    initListeners();
});